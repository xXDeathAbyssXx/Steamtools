						anuncio explicativo que confirma su logro y proporciona detalles sobre la cantidad de
						intentos realizados. Esta dinámica facilita la participación del estudiante, y brinda al
						docente la oportunidad de utilizar estos datos para evaluaciones sumativas.
					</p></span
				>

